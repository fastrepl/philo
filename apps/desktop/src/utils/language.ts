export function getLanguageDisplayName(code: string,) {
  try {
    return displayNames.of(code,) ?? code;
  } catch {
    try {
      return displayNames.of(new Intl.Locale(code,).language,) ?? code;
    } catch {
      return code;
    }
  }
}

export const SUPPORTED_SPOKEN_LANGUAGE_CODES = [...WHISPER_MULTILINGUAL_LANGUAGE_CODES,].sort((a, b,) =>
  getLanguageDisplayName(a,).localeCompare(getLanguageDisplayName(b,), "en",)
);
